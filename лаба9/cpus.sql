dot_pitch DECIMAL(3,2) check (0.2<=dot_pitch OR dot_pitch<0.3),
width DECIMAL(4) check (800<=width OR width<2000),
height DECIMAL(4) check (600<=height or height<2000),
price DECIMAL(4) check(price > 0)
)

